              <Link
                to="/appointments"
                className="block w-full bg-blue-600 text-white py-2 px-4 rounded-lg hover:bg-blue-700 transition-colors text-center font-semibold"
              >
                Schedule Appointment Online
              </Link>
              <a
                href="tel:5551234567"
                className="block w-full bg-gray-600 text-white py-2 px-4 rounded-lg hover:bg-gray-700 transition-colors text-center font-semibold"
